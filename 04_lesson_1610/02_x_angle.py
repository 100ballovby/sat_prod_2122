#  и рисующий любой многоугольник по количеству углов х

x = int(input('Количество углов: '))

for line in range(x):
    t.fd(100)  # идти вперед
    t.rt(360 / x)  # повернуть вправо
    t.stamp()  # оставить отпечаток черепашки

done()  # окно не должно закрываться само
